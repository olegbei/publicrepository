first_number = float(input('Please enter first number '))
second_number = float(input('Please enter second number '))

print(first_number + second_number, "this is addition")
print(first_number - second_number, "this is subtraction")
print(first_number * second_number, "this is multiplication")
print(first_number / second_number, "this is divine")
print(first_number // second_number, "this is Целочисленное деление")
print(first_number % second_number, "this is Деление по модулю")
print(first_number ** second_number, "this is Возведение в степень")

